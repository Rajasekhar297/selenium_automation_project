package selenium_automation_project;

public class string_class {

	public static void main(String[] args) {
		
		String sport = "Kabbadi";
		
		//".length()" is the method to find the size of the string 
		//".length" variable applicable for array concept
		
		System.out.println(sport.length());
		// Converstion
		//1- toUpperCase
		//2- toLowerCase
		
		System.out.println(sport.toLowerCase());
		
		System.out.println(sport.toUpperCase());
		
		
		
		
	}

}
